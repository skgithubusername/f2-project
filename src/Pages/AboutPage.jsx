import React, { useEffect, useState } from 'react';
import axios from 'axios';
import Navbar from '../component/Navbar';
import Footer from '../component/Footer';

function AboutPage() {
  const [aboutData, setAboutData] = useState(null);

  useEffect(() => {
    const fetchData = async () => {
      try {
        const response = await axios.get('/api/abouts/allAbouts');
        setAboutData(response.data);
      } catch (error) {
        console.error('Error fetching about data:', error);
      }
    };

    fetchData();
  }, []);

  if (!aboutData) {
    return <div>Loading...</div>;
  }

  return (
    <div
      className="about-page-container"
      style={{
        position: 'relative',
        minHeight: '100vh',
        width: '100%',
        display: 'flex',
        flexDirection: 'column',
        justifyContent: 'space-between',
        backgroundPosition: 'center',
        backgroundSize: 'cover',
        backgroundImage: "url(https://s3-alpha-sig.figma.com/img/c430/2178/0a87a56755e029f20d10a339a9f6e264?Expires=1721001600&Key-Pair-Id=APKAQ4GOSFWCVNEHN3O4&Signature=dn79GEh6BiULh6FtnUGpcuqo4g6qvHOO9-Fg9D4iYDI0Iiu-TjvULnYrnLkUCtsOtcgQQ8mjLPuFNYPAAgXW2yyxtEZDLl8qFQ8Fn-dSt0j98gvdRR3MlORNt7hasENv-CdOEPevuJ5GmeArETDo8bGGy3jr55sO0SDpqAzMe8Grvzk7MpabGbMJnVWzgr0yf~HC7htL2nWAopGzO8gV9ASxC5rqnd2QRVKuJFppRFdwQGnZpal35TtS6XiJ1wkvKtcPgBPORPgxZLv2oNcLqQcKBl6BtXKHFkYS~SuF5MbWVgFlhl6aZ9U2zQg-1mklfTj-fJn8pGHS8PWA7l9rRw__)",
      }}
    >
      <div
        style={{
          position: 'absolute',
          top: 0,
          left: 0,
          right: 0,
          bottom: 0,
          backgroundColor: 'rgba(2, 23, 75, 0.50)', // 50% opacity
          zIndex: 1,
        }}
      />
      <Navbar />
      <div style={{ position: 'relative', zIndex: 2, padding: '4rem 1rem', flex: '1' }}>
        <div className="flex flex-col m-4 md:m-10 lg:m-20">
          <h2 className="text-white font-semibold lg:text-3xl text-2xl mb-6">About</h2>
          <p className="text-white lg:font-semibold">
            {aboutData.title || 'No title available'}
          </p>
        </div>
        <div className="flex flex-col m-4 md:m-10 lg:m-20">
          <h2 className="text-white font-semibold lg:text-3xl text-2xl mb-6">Mission</h2>
          <p className="text-white lg:font-semibold">
            {aboutData.desc || 'No description available'}
          </p>
        </div>
        <div className="flex flex-col m-4 md:m-10 lg:m-20">
          <h2 className="text-white font-semibold lg:text-3xl text-2xl mb-6">Vision</h2>
          <p className="text-white lg:font-semibold">
            {aboutData.subdesc || 'No sub-description available'}
          </p>
        </div>
      </div>
      <Footer />
    </div>
  );
}

export default AboutPage;
